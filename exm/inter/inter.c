#include <unistd.h>

void	print(char *s1, char *s2)
{
	int s[256] = {0};
	int i;

	i = 0;
	while (s2[i])
	{
		s[(int)s2[i]] = 1;
		i++;
	}
	i = 0;
	while(s1[i])
	{
		if(s[(int)s1[i]] == 1)
		{
			write(1, &s1[i], 1);
			s[(int)s1[i]] = 2;
		}
		i++;
	}
}


int main(int ac, char **av)
{
	if (ac == 3)
	{
		print(av[1], av[2]);
	}
	write(1, "\n", 1);
}
