#include<unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	v[7];

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			v[0] = (i / 10) + 48;
			v[1] = (i % 10) + 48;
			v[2] = ' ';
			v[3] = (j / 10) + 48;
			v[4] = (j % 10) + 48;
			v[5] = ',';
			v[6] = ' ';
			if (i != 98)
				write(1, v, 7);
			else
				write(1, v, 5);
			j++;
		}
		i++;
	}
}
